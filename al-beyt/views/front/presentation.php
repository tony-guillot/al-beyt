                            <h1>Le projet solidaire</h1>
                            <p>
                            Le projet solidaire, moteur d’Al-Beyt, concrétise notre volonté d’apporter du soutien
                            par nous-mêmes aux personnes restées en Syrie.
                            Cet objectif réinvestit du sens dans nos existences en tant qu’éxilé-es politiques.
                            Ce projet solidaire prendra de nombreuses formes au long terme : favoriser la produc-tion artistique,
                            l’éducation... mais notre premier objectif est de rendre aux syrien-nes leurs propres terres.
                            Tous les bénéfices récoltés lors de nos évènements sont dédiés à l’achat d’un terrain agricole
                            dans la région d’Idleb. Plus d’1 million de déplacé-es intérieurs se trouvent immobilisé-es dans
                            cette région dépourvue de structures institutionnelles ou humanitaires. Cette zone n’est contrôlée
                            pas contrôle par le régime, beaucoup d’activistes et opposant-es politiques se trouvent alors à Idleb.
                            Acheter un terrain agricole dans cette région permettrait à plusieurs familles d’en faire leur lieu de
                            travail et donc de subsistance, de gagner en autonomie. Ce terrain sera un espace de travail partagé,
                            autogéré par plusieurs habitant.es de cette région. Les graines seront exportées par Al-Beyt, et non achetées
                            au régime qui détient le monopole des semances en Syrie. Ce projet s’inscrit bien sûr dans une perspective
                            d’écologie durable favorisant des techniques de cultures saines. 
                            L’achat et la gestion seront tenus à distance grâce aux nombreux contacts que Kassem et Moawea ont déjà dans
                            cette zone et nous entrerons en collaboration directe avec d'autres associations spécialisées dans le travail
                            de la terre dès l'achat du terrain.
                            </p>
